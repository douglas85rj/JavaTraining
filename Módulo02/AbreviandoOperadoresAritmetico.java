
public class AbreviandoOperadoresAritmetico {

	public static void main(String [] args) {

  	int quantidadeProdutos = 10;
	
	quantidadeProdutos = quantidadeProdutos +3;	
	System.out.println(quantidadeProdutos);
	//Forma abreviada
	quantidadeProdutos += 3;
	System.out.println(quantidadeProdutos);
        quantidadeProdutos -= 3;
	System.out.println(quantidadeProdutos);
	quantidadeProdutos *= 3;
	System.out.println(quantidadeProdutos);
	quantidadeProdutos /= 3;
	System.out.println(quantidadeProdutos);
	quantidadeProdutos %= 3;
	System.out.println(quantidadeProdutos);
}
}