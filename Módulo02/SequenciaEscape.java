
public class SequenciaEscape {

	public static void main(String[]args) {

	//Utilizar o \ para escape antes e depois do nome Maria, para que o print saia 	com o nome entre aspas.
	System.out.println("Oi \"Maria\"");
		
	// Escape \n para quebrar a linha
	System.out.println("Seu nome: \n \"João\"");
	
	// o \\ se torna uma única quando precisa imprimi-la.
	System.out.println("C:\\Windows");

	}
}